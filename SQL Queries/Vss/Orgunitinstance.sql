SELECT
  main.ORGUNITINSTANCE.KBUSINESSENTITYID,
  main.ORGUNITINSTANCE.FNEWBUSINESSENTITYID,
  main.ORGUNITINSTANCE.STARTDATE,
  main.ORGUNITINSTANCE.FORGUNITNUMBER,
  main.ORGUNITINSTANCE.FSITEORGUNITNUMBER,
  main.ORGUNITINSTANCE.ENDDATE,
  main.ORGUNITINSTANCE.FMANAGERTYPECODEID,
  main.ORGUNITINSTANCE.PLANNEDRESTRUCTUREDATE,
  main.ORGUNITINSTANCE.FMANAGERTYPECODE,
  main.ORGUNITINSTANCE.LOCKSTAMP,
  main.ORGUNITINSTANCE.AUDITDATETIME,
  main.ORGUNITINSTANCE.FAUDITSYSTEMFUNCTIONID,
  main.ORGUNITINSTANCE.FAUDITUSERCODE,
  main.X000_Orgunit.KORGUNITNUMBER AS KORGUNITNUMBER1,
  main.X000_Orgunit.STARTDATE AS STARTDATE1,
  main.X000_Orgunit.ENDDATE AS ENDDATE1,
  main.X000_Orgunit.FORGUNITTYPECODEID,
  main.X000_Orgunit.FORGUNITTYPECODE,
  main.X000_Orgunit.ISSITE,
  main.X000_Orgunit.LOCKSTAMP AS LOCKSTAMP1,
  main.X000_Orgunit.AUDITDATETIME AS AUDITDATETIME1,
  main.X000_Orgunit.FAUDITSYSTEMFUNCTIONID AS FAUDITSYSTEMFUNCTIONID1,
  main.X000_Orgunit.FAUDITUSERCODE AS FAUDITUSERCODE1,
  main.X000_Orgunit.KSTARTDATE,
  main.X000_Orgunit.ENDDATE1 AS ENDDATE11,
  main.X000_Orgunit.KORT,
  main.X000_Orgunit.LANK,
  main.X000_Orgunit.SHORT,
  main.X000_Orgunit.LONG,
  main.X000_Orgunit.UNIT_TYPE,
  main.X000_Codedescription.LONG AS MANAGER_TYPE
FROM
  main.ORGUNITINSTANCE
  LEFT JOIN main.X000_Orgunit ON main.X000_Orgunit.KORGUNITNUMBER = main.ORGUNITINSTANCE.FORGUNITNUMBER
  LEFT JOIN main.X000_Codedescription ON main.X000_Codedescription.KCODEDESCID = main.ORGUNITINSTANCE.FMANAGERTYPECODEID
