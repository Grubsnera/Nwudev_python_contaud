SELECT
  X000_PER_ALL_PEOPLE.EMPLOYEE_NUMBER,
  X001_ASSIGNMENT_01_PERI.ASS_ID,
  X001_ASSIGNMENT_01_PERI.PERSON_ID,
  X001_ASSIGNMENT_01_PERI.ASS_NUMB,
  X000_PER_ALL_PEOPLE.FULL_NAME,
  X000_PER_ALL_PEOPLE.KNOWN_NAME,
  X000_PER_ALL_PEOPLE.DATE_OF_BIRTH,
  X001_ASSIGNMENT_01_PERI.EMP_START,
  X001_ASSIGNMENT_01_PERI.ASS_START,
  X001_ASSIGNMENT_01_PERI.ASS_END,
  X001_ASSIGNMENT_01_PERI.EMP_END,
  X001_ASSIGNMENT_01_PERI.LEAVING_REASON,
  X001_ASSIGNMENT_01_PERI.LEAVE_REASON_DESCRIP,
  X001_ASSIGNMENT_01_PERI.LOCATION_DESCRIPTION,
  X001_ASSIGNMENT_01_PERI.ORG_TYPE_DESC,
  X001_ASSIGNMENT_01_PERI.OE_CODE,
  X001_ASSIGNMENT_01_PERI.GRADE,
  X001_ASSIGNMENT_01_PERI.GRADE_NAME,
  X001_ASSIGNMENT_01_PERI.POSITION,
  X001_ASSIGNMENT_01_PERI.POSITION_NAME,
  X001_ASSIGNMENT_01_PERI.ORG_NAME,
  X001_ASSIGNMENT_01_PERI.JOB_NAME,
  X001_ASSIGNMENT_01_PERI.JOB_SEGMENT_NAME,
  X001_ASSIGNMENT_01_PERI.ACAD_SUPP,
  X001_ASSIGNMENT_01_PERI.EMPLOYMENT_CATEGORY,
  X001_ASSIGNMENT_01_PERI.LEAVE_CODE,
  X001_ASSIGNMENT_01_PERI.SUPERVISOR_ID,
  X000_PER_ALL_PEOPLE1.EMPLOYEE_NUMBER AS SUPERVISOR,
  X001_ASSIGNMENT_01_PERI.ASS_WEEK_LEN,
  X001_ASSIGNMENT_01_PERI.ASS_ATTRIBUTE2,
  X000_PER_ALL_PEOPLE.NATIONALITY,
  X000_PER_ALL_PEOPLE.NATIONALITY_NAME,
  X000_PER_ALL_PEOPLE.USER_PERSON_TYPE,
  X001_ASSIGNMENT_01_PERI.PRIMARY_FLAG,
  X000_PER_ALL_PEOPLE.CURRENT_EMPLOYEE_FLAG,
  X001_ASSIGNMENT_01_PERI.DATE_ASS_LOOKUP,
  X001_ASSIGNMENT_01_PERI.ASS_ACTIVE,
  X001_ASSIGNMENT_01_PERI.DATE_EMP_LOOKUP,
  X001_ASSIGNMENT_01_PERI.EMP_ACTIVE,
  X001_ASSIGNMENT_01_PERI.GRADE_CALC,
  X000_COUNTS.COUNT_ASS,
  X000_COUNTS.COUNT_PEO,
  X000_COUNTS.COUNT_POS
FROM
  X001_ASSIGNMENT_01_PERI
  LEFT JOIN X000_PER_ALL_PEOPLE ON X000_PER_ALL_PEOPLE.PERSON_ID = X001_ASSIGNMENT_01_PERI.PERSON_ID AND
    X000_PER_ALL_PEOPLE.EFFECTIVE_START_DATE <= X001_ASSIGNMENT_01_PERI.DATE_ASS_LOOKUP AND
    X000_PER_ALL_PEOPLE.EFFECTIVE_END_DATE >= X001_ASSIGNMENT_01_PERI.DATE_ASS_LOOKUP
  LEFT JOIN X000_PER_ALL_PEOPLE X000_PER_ALL_PEOPLE1 ON X000_PER_ALL_PEOPLE1.PERSON_ID =
    X001_ASSIGNMENT_01_PERI.SUPERVISOR_ID AND X000_PER_ALL_PEOPLE1.EFFECTIVE_START_DATE <=
    X001_ASSIGNMENT_01_PERI.DATE_ASS_LOOKUP AND X000_PER_ALL_PEOPLE1.EFFECTIVE_END_DATE >=
    X001_ASSIGNMENT_01_PERI.DATE_ASS_LOOKUP
  LEFT JOIN X000_COUNTS ON X000_COUNTS.PERSON_ID = X001_ASSIGNMENT_01_PERI.PERSON_ID
ORDER BY
  X000_PER_ALL_PEOPLE.EMPLOYEE_NUMBER,
  X001_ASSIGNMENT_01_PERI.EMP_START DESC
