Select
    x002p.EMPLOYMENT_CATEGORY,
    x002p.PERSON_TYPE,
    x002p.ACAD_SUPP,
    Count(x002p.ASS_ID) As PERSON_TYPE_COUNT
From
    X002_PEOPLE_CURR x002p
Group By
    x002p.EMPLOYMENT_CATEGORY,
    x002p.PERSON_TYPE,
    x002p.ACAD_SUPP
Order By
    x002p.EMPLOYMENT_CATEGORY,
    x002p.PERSON_TYPE,
    x002p.ACAD_SUPP